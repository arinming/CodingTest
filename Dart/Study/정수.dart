void main() {
  int age = 20;
  print(age);

  double longitude = 127.634324;
  print(longitude);

  print(age.toDouble());

  print(longitude.toInt()); // 소수점 뒤에 숫자를 버림

  print(1 + 2); // 덧셈
  print(2 * 4); // 곱셈
  print(4 / 3); // 나눗셈 1.333...
  print(5 % 3); // 5를 3으로 나눈 나머지 , 2
  print(5 ~/ 3); // 5를 3으로 나눈 몫 , 1
}
